const imageFilter = (req, file, cb) => {
  if (
    file.mimetype === 'image/png' ||
    file.mimetype === 'image/jpg' ||
    file.mimetype === 'image/jpeg'
  ) {
    cb(null, true)
  } else {
    cb(null, false)
  }
}

const MAX_FILE_SIZE = 2000000 // 2mb

module.exports = { imageFilter, MAX_FILE_SIZE }
